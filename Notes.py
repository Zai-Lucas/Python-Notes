# Math operators, there are six
# +, -, *, /, **, (exponents), % (module divison)
print(4 + 8) #12
print(8 - 2) #6
print(-3 * -2) #6
print(6 /4) #1.5
print(2 ** 4) #16
print(12 % 5) #2
print(6 % 3) #0
print(16 % 9) #7

